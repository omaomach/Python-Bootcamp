# Python supports several built-in data types:
#   Integers (int): Whole numbers (e.g, 10, -5)
#   Floats (float): Decimal numbers (e.g, 3.14, -0.001)
#   Strings (str): Text data enclosed in quotes (e.g, "Comraid", "Fintech")
#   Booleans (bool): Represents True of False
#   Lists: Ordered, mutable collections (e.g, [1, 2, 3])
#   Tuples: Ordered, immutable collections (e.g, (1, 2, 4))
#   Sets: Unordered collections of unique elements (e.g, {1, 2, 3})
#   Dictionaries: Key-value pairs (e.g, {"name": "Alice", "age": 50})

age = 3
print(age)
print(type(age))

balance = 50.5
print(balance)
print(type(balance))

name = "Comraid"
print(name)
print(type(name))

is_completed = False
print(is_completed)
print(type(is_completed))